                                    <p class="mb-5">
                                    Compostable plastic” as defined in Rule 3(e) of PWM Rules, means “plastic that undergoes degradation by biological processes during composting to yield CO2, water, inorganic compounds and biomass at a rate consistent with other known compostable materials, excluding conventional petro-based plastics, and does not leave visible, distinguishable or toxic residue.”As per the Rule 4(h) (Conditions) of PWM Rules, 2016, the manufacturers or sellers of compostable plastic  commodities  have to obtain certificate from the Central Pollution Control Board before marketing or selling compostable carry bags. CPCB updated the Standard Operating Procedure <a href="http://164.100.43.186:8080/compostable/pdf/SOP-IssueCert-CompostablePlasticManufacturers.pdf" target="_blank">Link</a> for certification of Compostable commodities . This web- application provides for certification of compostable plastic manufacturers in-line with the provisions of the SOP.  
